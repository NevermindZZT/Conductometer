#include	"pwm.h"

/*******************************************************************************
*函数名称： PWM_Init
*功能：     PWM初始化
*参数：     无
*返回值：   无
*说明：     初始化PWM定时器，设置频率
*******************************************************************************/
void PWM_Init(void)
{
	BASICTIM4_Config(10000, 719);				//10Hz
}


/*******************************************************************************
*函数名称： PWM_SetDutyCycle
*功能：     设置PWM占空比
*参数：     dutyCycle       占空比
*返回值：   无
*******************************************************************************/
void PWM_SetDutyCycle(float dutyCycle)
{
	TIM_SetCompare4(TIM4, (uint16_t)(dutyCycle * 100 + 0.5));
}
